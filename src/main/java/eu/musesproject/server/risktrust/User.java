/**
 * The Class User.
 * 
 * @author Jean-Marc Seigneur (UNIGE)
 * @version Sep 24, 2013
 */
public class User {
	
	private UserTrustValue usertrustvalue;
	
	private String userId;

	private String email;

	private int enabled;

	private String name;

	private String password;

	private int roleId;

	private String surname;

	private String username;

	/**
	 * @return the usertrustvalue
	 */
	public UserTrustValue getUsertrustvalue() {
		return usertrustvalue;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getEnabled() {
		return enabled;
	}

	public void setEnabled(int enabled) {
		this.enabled = enabled;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public int getRoleId() {
		return roleId;
	}

	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}

	public String getSurname() {
		return surname;
	}

	public void setSurname(String surname) {
		this.surname = surname;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	/**
	 * @param usertrustvalue the usertrustvalue to set
	 */
	public void setUsertrustvalue(UserTrustValue usertrustvalue) {
		this.usertrustvalue = usertrustvalue;
	}

}
